#Arithmetic operators 
num1 = 10
num2 = 3
print("num1 + num2 is ", num1+num2)
print("num1 - num2 is ", num1-num2)
print("num1 * num2 is ", num1*num2)
print("num1 / num2 is ", num1/num2)
print("num1 // num2 is ", num1//num2)
print("num1 ** num2 is ", num1**num2)
print("num1 % num2 is ", num1%num2)

